package Convo;

public class MainClass {
    public static void main(String[] args) {
        convocation c1 = new convocation(333, "2025");
        convocation c2 = new convocation(123, "2024");
        convocation c3 = new convocation(777, "2026");

        // Add some students
        c1.setStudentIds(new int[]{1, 2, 3, 4, 5});
        c2.setStudentIds(new int[]{10, 20, 30});
        c3.setStudentIds(new int[]{11, 22, 33, 44});

        // Calculate total cost (extra amount varies)
        c1.calculateConvocationTotalCost(500);
        c2.calculateConvocationTotalCost(1000);
        c3.calculateConvocationTotalCost(700);

        // Display info
        c1.displayInfo();
        c2.displayInfo();
        c3.displayInfo();

        // Display static count
        convocation.displayTotalConvocationObjectsCreatedSoFar();

        // Calculate sum of total costs of IDs with same digits
        double sum = c1.SumOfConvocationTotalCosts(c1, c2, c3);
        System.out.println("Sum of Total Costs (IDs with same digits): Rs. " + sum);
    }
}
