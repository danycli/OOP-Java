package casting;
import java.util.Date;
public class typecasting {
    public static void main(String [] args){
        int a = 4;
        double c = a/7.0;
        System.out.println(c);
        System.out.println(Math.max(a,c));
        Date g = new Date();
        g.getTime();
        System.out.println(g);
    }
}
